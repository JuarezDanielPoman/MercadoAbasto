    private void limpiarCampos(){
        nombreSectorTextField.setText(null);
        limitePuestosTextField.setText(null);
        precioMetroCuadradoTextField.setText(null);
        porcentajeGeneralTextField.setText(null);
        porcentajeTechoTextField.setText(null);
        porcentajeCamaraTextField.setText(null);
    }
    
    private Boolean controlarCamposVacios(){
        //controla que haya texto en los campos, cambiando el cartel mostrado dependiendo del dato faltante
        
        if(nombreSectorTextField.getText().isEmpty() && limitePuestosTextField.getText().isEmpty() && precioMetroCuadradoTextField.getText().isEmpty() && porcentajeGeneralTextField.getText().isEmpty() && porcentajeTechoTextField.getText().isEmpty() && porcentajeCamaraTextField.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Los campos no pueden estar vacios");
        } else {        
            if(nombreSectorTextField.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "El nombre del sector no puede estar vacio");
            }
            if(limitePuestosTextField.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "La cantidad de puestos no puede estar vacia");
            }
            if(precioMetroCuadradoTextField.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "El precio por metro cuadrado no puede estar vacio");
            }
            if(porcentajeGeneralTextField.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "El porcentaje general no puede estar vacio");
            }
            if(porcentajeTechoTextField.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "El porcentaje por el techo no puede estar vacio");
            }
            if(porcentajeCamaraTextField.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "El porcentaje por camara frigorifica no puede estar vacio");
            }
        }
        if(nombreSectorTextField.getText().isEmpty() || limitePuestosTextField.getText().isEmpty() || precioMetroCuadradoTextField.getText().isEmpty() || porcentajeGeneralTextField.getText().isEmpty() || porcentajeTechoTextField.getText().isEmpty() || porcentajeCamaraTextField.getText().isEmpty()){
            return false;
        }
        return true;
    }
    
    private Boolean controlarNumerosIngresados(){
        if(Integer.parseInt(limitePuestosTextField.getText()) == 0){
            JOptionPane.showMessageDialog(null, "El limite de puestos debe ser mayor a 0");
        }
        if(Integer.parseInt(precioMetroCuadradoTextField.getText()) == 0){
            JOptionPane.showMessageDialog(null, "El precio por metro cuadrado debe ser mayor a 0");
        }
        if(Integer.parseInt(porcentajeGeneralTextField.getText()) == 0){
            JOptionPane.showMessageDialog(null, "El porcentaje general debe ser mayor a 0");
        }
        if(Integer.parseInt(porcentajeTechoTextField.getText()) == 0){
            JOptionPane.showMessageDialog(null, "El porcentaje por el techo debe ser mayor a 0");
        }
        if(Integer.parseInt(porcentajeCamaraTextField.getText()) == 0){
            JOptionPane.showMessageDialog(null, "El porcentaje por camara frigorifica debe ser mayor a 0");
        }
        if(Integer.parseInt(limitePuestosTextField.getText())==0 || Integer.parseInt(precioMetroCuadradoTextField.getText())==0 || Integer.parseInt(porcentajeGeneralTextField.getText())==0 || Integer.parseInt(porcentajeTechoTextField.getText())==0 || Integer.parseInt(porcentajeCamaraTextField.getText())==0){
                return false;
        }
        return true;
    }
