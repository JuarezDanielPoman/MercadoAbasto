    private void guardarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarButtonActionPerformed

        try{
            if(controlarCamposVacios()){
                if(controlarNumerosIngresados()){
                    String nombreSector = nombreSectorTextField.getText().toUpperCase();
                    Integer limitePuestos = Integer.parseInt(limitePuestosTextField.getText());
                    BigDecimal precioMetroCuadrado = new BigDecimal(Integer.parseInt(precioMetroCuadradoTextField.getText()));
                    Integer porcentajeGeneral = Integer.parseInt(porcentajeGeneralTextField.getText());
                    Integer porcentajeTecho = Integer.parseInt(porcentajeTechoTextField.getText());
                    Integer porcentajeCamara = Integer.parseInt(porcentajeCamaraTextField.getText());

                    Sector nuevoSector = new Sector(nombreSector, limitePuestos, precioMetroCuadrado, porcentajeGeneral, porcentajeTecho, porcentajeCamara);
                    this.mercado.agregarSector(nuevoSector);

                    MenuPrincipal.modeloCombo.addElement(nuevoSector.getNombreSector());
                    JOptionPane.showMessageDialog(null,"SECTOR AGREGADO CORRECTAMENTE");

                    limpiarCampos();
                }
            }
        } catch (SectorExistenteException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        } catch (LimiteSectoresException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Solo se permiten letras en el nombre del sector");
        }
    }//GEN-LAST:event_guardarButtonActionPerformed

