    private void btnDetallesContratosPuestosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetallesContratosPuestosActionPerformed
        if(clienteSeleccionado!=null){
            if(verContratosPuestosDelCliente == null || verContratosPuestosDelCliente.isClosed()){
                verContratosPuestosDelCliente = new VerContratosPuestosDelCliente(mercado, clienteSeleccionado);
                PanelEscritorio.add(verContratosPuestosDelCliente);
                Dimension desktopSize = PanelEscritorio.getSize();
                Dimension FrameSize = verContratosPuestosDelCliente.getSize();
                verContratosPuestosDelCliente.setLocation((desktopSize.width - FrameSize.width)/2, (desktopSize.height- FrameSize.height)/2);  
                verContratosPuestosDelCliente.show();
            }else{
                 JOptionPane.showMessageDialog(null, "La Ventana Se Encuentra Abierta");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un cliente para poder ver sus contratos");
        }
    }//GEN-LAST:event_btnDetallesContratosPuestosActionPerformed

