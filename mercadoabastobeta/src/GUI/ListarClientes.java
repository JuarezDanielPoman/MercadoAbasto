    private void btnDetallesClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetallesClienteActionPerformed
        if(clienteSeleccionado != null){
            if(detallesDelCliente == null || detallesDelCliente.isClosed()){
                detallesDelCliente = new DetallesDelCliente(this.mercado, modeloTablaClientes, clienteSeleccionado);
                PanelEscritorio.add(detallesDelCliente);
                Dimension desktopSize = PanelEscritorio.getSize();
                Dimension FrameSize = detallesDelCliente.getSize();
                detallesDelCliente.setLocation((desktopSize.width - FrameSize.width)/2, (desktopSize.height- FrameSize.height)/2);  
                detallesDelCliente.show();
            }else{
                 JOptionPane.showMessageDialog(null, "La Ventana Se Encuentra Abierta");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un cliente para poder ver sus detalles");
        }
    }//GEN-LAST:event_btnDetallesClienteActionPerformed

    private void tablaClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaClientesMouseClicked
        try {
            String seleccion = tablaClientes.getValueAt(tablaClientes.getSelectedRow(),ModelTableCliente.CUILCUIT).toString();
            clienteSeleccionado = this.mercado.buscarCliente(seleccion);
        } catch (ClienteInexistenteException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_tablaClientesMouseClicked


